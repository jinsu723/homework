// 3-13 : 관계 연산자 1
package section03;

public class EX03_13 {
	public static void main(String[] args) {
		int a = 10;  // 변수 선언하고 값 저장
		int b = 20;  // 변수 선언하고 값 저장

		System.out.println(a > b);  // a가 b보다 큰가
		System.out.println(a <= b);  // a가 b보다 작거나 같은가
		System.out.println(a == b);  // a가 b와 같나
		System.out.println(a != b);  // a가 b와 같지 않나
	}
}
