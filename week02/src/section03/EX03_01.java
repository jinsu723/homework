// 3-1 : 부호 연산자
package section03;

public class EX03_01 {
	public static void main(String[] args) {
		int x = 100;  // 정수형 변수 선언 후 변수 저장
		int resultPlus = +x;  // 변수 선언 후 x값을 더해서 저장
		int resultMinus = -x;  // 변수 선언 후 x값을 빼서 저장

		System.out.println(resultPlus);  // 저장된 값을 춮력
		System.out.println(resultMinus);  // 저장된 값을 출력
	}
}
