// 3-14 : 관계 연산자 2
package section03;

public class EX03_14 {
	public static void main(String[] args) {
		int a = 10;  // 변수 선언하고 값 저장
		int b = 11;  // 변수 선언하고 값 저장

		System.out.println(a <= b);  // a가 b보다 같거나 작나
		System.out.println(a == b);  // a와 b가 같나
		System.out.println(a != --b);  // a가 b에서 1을 뺀 값과 같지 않나
	}
}
