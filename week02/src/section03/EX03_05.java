// 3-5 : 증감 연산자 3
package section03;

public class EX03_05 {
	public static void main(String[] args) {
		int num = 30;  // 변수 선언 후 저장
		System.out.println(++num);  // 1을 더하고 변수 출력
		System.out.println(num);  // 저장된 값을 출력

		System.out.println(num++);  // 저장된 값을 출력하고 1을 더함
		System.out.println(num);  // 저장된 변수 출력
	}
}
