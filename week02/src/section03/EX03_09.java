package section03;

public class EX03_09 {
	public static void main(String[] args) {
		int x = 100;  // 변수를 선언하고 값 저장
		int y = 200;  // 변수를 선언하고 값 저장

		System.out.println(x + y);  // 두 변수를 더한 값 출력
		System.out.println(x - y);  // x에서 y를 뺀 값 출력
		System.out.println(x * y);  // 두 변수를 곱한 값 출력
		System.out.println(x / y);  // x나누기 y의 값 출력
		System.out.println(x % y);  // x나누기 y의 나머지 값 출력
	}
}
