// 2-11 : 정수형(1)
package section02;

public class EX02_11 {
	public static void main(String[] args) {
		byte num1 = 20;  // byte형 정수에 값 저장
		short num2 = 30;  // short형 변수에 값 저장
		int num3 = 40;  // int형 변수에 값 저장
		long num4 = 50;  // long형 변수에 값 저장

		System.out.println(num1);  // 변수에 저장된 값 출력
		System.out.println(num2);  // 변수에 저장된 값 출력
		System.out.println(num3);  // 변수에 저장된 값 출력
		System.out.println(num4);  // 변수에 저장된 값 출력
	}
}