// 2-14 : 실수형(2)<정밀도 확인하기>
package section02;

public class EX02_14 {
	public static void main(String[] args) {
		double pieDouble = 3.141592653589793;  // 실수형 변수를 선언하고 값 저장
		float pieFloat = 3.141592653589793f;  // 실수형 변수를 선언하고 값 저장

		System.out.println("double : " + pieDouble);  // 변수에 저장된 값 출력
		System.out.println("float : " + pieFloat);  // 변수에 저장된 값 출력
	}
}