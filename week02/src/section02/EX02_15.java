package section02;

public class EX02_15 {
	public static void main(String[] args) {
		char ga = '가';  // 변수 선언하고 값 저장
		char na = '나';  // 변수 선언하고 값 저장
		char alphabetA = 'A';  // 변선언하고 값 저장
		char alphabetB = 'B';  // 변수 선안하고 값 저장

		System.out.println(ga);  // 변수에 저장된 값 출력
		System.out.println(na);  // 변수에 저장된 값 출력
		System.out.println(alphabetA + ", " + alphabetB);  // 변수에 저장된 값 2개 출력
	}
}