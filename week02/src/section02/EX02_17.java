package section02;

public class EX02_17 {
	public static void main(String[] args) {
		int alphabetA = 'A';  // 정수형 변수에 유니코드 값 저장
		int alphabetB = 'B';  // 정수형 변수에 유니코드 값 저장
		System.out.println(alphabetA);  // 변수에 저장된 값 출력
		System.out.println(alphabetB);  // 변수에 저장된 값 출력

		char alphabetC = 67;  // 문자형 변수에 유니코드 값 저장
		System.out.println(alphabetC);  // 변수에 저장된 값 출력
	}
}