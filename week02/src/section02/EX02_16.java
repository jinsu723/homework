package section02;

public class EX02_16 {
	public static void main(String[] args) {
		int ga = '가';  // 정수형 변수에 유니코드값 저장
		int na = '나';  // 정수형 변수에 유니코드값 저장

		int alphabetA = 'A';  // 정수형 변수에 유니코드값 저장
		int alphabetB = 'B';  // 정수형 변수에 유니코드값 저장

		System.out.println(ga);  // 변수에 저장된 값 출력
		System.out.println(na);  // 벼수에 저장된 값 출력
		System.out.println(alphabetA);  // 변수에 저장된 값 출력
		System.out.println(alphabetB);  // 변수에 저장된 값 출력
	}
}