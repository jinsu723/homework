package section02;

public class EX02_19 {
	public static void main(String[] args) {
		boolean isStudent = true;  // 논리형 변수 선언하고 값 저장

	if(isStudent) {  // ()안의 값이 true라면
		System.out.println("저는 학생입니다.");  // 문자열 출력
	} else {  // false라면
		System.out.println("저는 학생이 아닙니다.");  // 문자열 출력
		}
	}
}