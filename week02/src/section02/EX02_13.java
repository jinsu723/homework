// 2-13 : 실수형(1)
package section02;

public class EX02_13 {
	public static void main(String[] args) {
		double marathon = 42.195;  // 실수형 변수를 선언하고 저장한다
		float halfMarathon = 21.0975f;  // 실수형 float 변수를 선언하고 저장한다 f를 붙여서 float형인 것을 알려준다

		System.out.println("마라톤은 " + marathon + "km를 달립니다.");  // 변수에 저장된 값 출력
		System.out.println("하프 마라톤은 " + halfMarathon + "km를 달립니다.");  // 변수에 저장된 값 출력
	}
}