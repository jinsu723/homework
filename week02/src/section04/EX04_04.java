// 4-4 : if-else 문(2)
package section04;

public class EX04_04 {
	public static void main(String[] args) {
		int a = 4;  // 변수 선언하고 값 저장
		int b = 10;  // 변수 선언하고 값 저장
		
		if(a > b) {  // a가 b보다 크다면
			System.out.println("a가 b보다 큽니다.");  // 메시지 출력
		} else {  // 아니라면
			System.out.println("a가 b보다 작거나 같습니다.");  // 메시지 출력
		}
	}
}
