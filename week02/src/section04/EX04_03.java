// 4-3 : if-else 문(1)
package section04;

public class EX04_03 {
	public static void main(String[] args) {
		int num = 5;  // 변수 선언하고 값 저장
		
		if(num > 4) {  // 변수가 4보다 크면
			System.out.println(num + "은(는) 4보다 큽니다.");  // 메시지 출력
		} else {  // 변수가 4보다 작으면
			System.out.println(num + "은(는) 4보다 작습니다.");  // 메시지 출력
		}
	}
}
